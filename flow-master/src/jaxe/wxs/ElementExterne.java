package jaxe.wxs;

import org.w3c.dom.Element;

import jaxe.Config;


/**
 * Représente un élément XML défini dans un schéma qui est référencé dans une autre configuration Jaxe.
 */
public class ElementExterne implements ToutElement {
    
    private Element ref;
    private Config cfg;
    
    public ElementExterne(final Element ref, final Config cfg) {
        this.ref = ref;
        this.cfg = cfg;
    }
    
    public Element getDOMElement() {
        return(ref);
    }
    
    public String getName() {
        return(cfg.nomElement(ref));
    }
    
    public String getNamespace() {
        return(cfg.espaceElement(ref));
    }
}
